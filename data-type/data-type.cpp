
#include <iostream>
using namespace std;

/*
Data Types:
Int - 4 bytes
Short - 2 bytes
Long - 4 bytes
Long long - 8 bytes
float - 4 bytes
double - 8 bytes
char - 1 byte
bool - 1 byte

*/

int main()
{
  // integer data type
  int num = 10;
  cout << "Integer: " << num << endl;

  // floating-point data type
  float fnum = 3.14;
  cout << "Float: " << fnum << endl;

  // double data type
  double dnum = 3.14159265358979323846;
  cout << "Double: " << dnum << endl;

  // character data type
  char ch = 'A';
  cout << "Character: " << ch << endl;

  // boolean data type
  bool flag = true;
  cout << "Boolean: " << flag << endl;

  // short data type
  short snum = 8247;
  cout << "Short: " << snum << endl;

  // long data type
  long lnum = 124578;
  cout << "Long: " << lnum << endl;

  return 0;
}
